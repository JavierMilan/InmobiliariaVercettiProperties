// ============================================================
//  Motor i18n
// ============================================================

const I18N = {

  lang: localStorage.getItem('vp_lang') || 'en',

  t(key) {
    return TRANSLATIONS[this.lang][key] ?? TRANSLATIONS['en'][key] ?? key;
  },

  apply() {
    // 1. data-i18n → textContent
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.dataset.i18n;
      const text = this.t(key);
      if (text.includes('\n')) {
        el.innerHTML = text.split('\n').join('<br>');
      } else {
        el.textContent = text;
      }
    });

    // 2. data-i18n-placeholder → placeholder
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      el.placeholder = this.t(el.dataset.i18nPlaceholder);
    });

    // 3. Opciones de select con data-i18n-opt
    document.querySelectorAll('option[data-i18n-opt]').forEach(opt => {
      opt.textContent = this.t(opt.dataset.i18nOpt);
    });

    // 4. Descripciones con data-i18n-desc
    document.querySelectorAll('[data-i18n-desc]').forEach(el => {
      const key = 'desc.' + el.dataset.i18nDesc;
      const text = TRANSLATIONS[this.lang][key];
      if (text) el.textContent = text;
    });
    // 5. Tipo de inmueble con data-i18n-tipo
    const tipoMap = {
        en: { 'Casa': 'House', 'Edificio': 'Building', 'Local': 'Commercial', 'Garage': 'Garage' },
        es: { 'Casa': 'Casa', 'Edificio': 'Edificio', 'Local': 'Local', 'Garage': 'Garaje' }
    };
    document.querySelectorAll('[data-i18n-tipo]').forEach(el => {
        const valor = el.dataset.i18nTipo;
        const traduccion = tipoMap[this.lang][valor];
        if (traduccion) el.textContent = traduccion;
    });

    // 6. Botón de idioma
    const btn = document.getElementById('lang-toggle');
    if (btn) {
      btn.textContent = this.lang === 'en' ? 'ES' : 'EN';
      btn.setAttribute('aria-label',
        this.lang === 'en' ? 'Cambiar a Español' : 'Switch to English');
    }

    // 7. lang del <html>
    document.documentElement.lang = this.lang === 'en' ? 'en' : 'es';
  },

  toggle() {
    this.lang = this.lang === 'en' ? 'es' : 'en';
    localStorage.setItem('vp_lang', this.lang);
    this.apply();
  },

  init() {
    this.apply();
    const btn = document.getElementById('lang-toggle');
    if (btn) btn.addEventListener('click', () => this.toggle());
  }
};

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', () => I18N.init());
} else {
  I18N.init();
}
